// @flow
import { createContext } from 'react';
import { removeResponse } from '../../../../redux';
import { EMPTY_STRING } from '../../../../config/constants';

const ApiContext = createContext<K>({

  id: EMPTY_STRING,
  name:EMPTY_STRING,
  leader: {},
  record: {},
  fields: [],
  template: {},

  set: K => K,
  get: () => K,
  update: K => () => K.update(K),
  remove: (dispatch) => dispatch(removeResponse(K.name)),
});

export default ApiContext;
